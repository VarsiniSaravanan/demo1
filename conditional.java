public class conditional{
    public static void main(String a[]){
        int x=78;
        int y=78;
        if(x>y){
            System.out.println(x);
            System.out.println("x is greater");
        }
        else if(x<y){
            System.out.println(y);
            System.out.println("y is greater");
        }
        else{
            System.out.println("x and y are equal");
        }
    }
}



